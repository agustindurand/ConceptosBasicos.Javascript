

/* METODO -            .push() */ 
/* El método .push() en JavaScript se utiliza para agregar uno o más elementos al final de un arreglo (array).

Cuando se llama al método .push() en un arreglo, se proporciona uno o más elementos como argumentos, y estos elementos se agregan al final del arreglo. El método .push() modifica el arreglo original y devuelve la nueva longitud del arreglo después de haber agregado los elementos. */ 


