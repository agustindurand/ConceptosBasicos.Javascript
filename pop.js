/* METODO -        .pop() 
El método .pop() en JavaScript se utiliza para eliminar el último elemento de un arreglo y devolver ese elemento eliminado.

Cuando se llama al método pop() en un arreglo, se elimina el último elemento del arreglo y se actualiza la longitud del arreglo. El valor eliminado es devuelto como resultado de la función pop(). Si el arreglo está vacío, el método pop() devolverá undefined.

*/ 

let estaciones = ["Primera", "Otoño", "Verano"]; 

estaciones.pop();

console.log(estaciones); 

/* Elimina Verano */ 


